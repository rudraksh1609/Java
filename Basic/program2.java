/*Loop Run*/
class program2 
{
    public static void main(String[] args) 
    {
    //loop till i<4
       for(int i=0; i<4; i++)
        {
            System.out.println(args[i]);
        }
    // loop till args[] length
        for(int i=0; i< args.length; i++)
        {
            System.out.println(args[i]);
        }
    }    
}
